import Head from "next/head";
import { useEffect, useMemo, useState } from "react";
import Navbar from "../components/Navbar";
import Projects from "../components/Projects";
import Resume from "../components/Resume";
import Services from "../components/Services";
import SocialMedia from "../components/SocialMedia";
import { UserContext } from "../Context/UserContext";
import { motion as m } from "framer-motion";
import { data } from "../data/data";

export default function Home() {
  const [darkMode, setDarkMode] = useState(false);
  const darkModeState = (darkMode) => {
    setDarkMode(darkMode);
    window.localStorage.setItem("darkModeState", JSON.stringify(darkMode));
  };
  useEffect(() => {
    let currentState = window.localStorage.getItem("darkModeState");
    setDarkMode(JSON.parse(currentState));
  }, []);

  return (
    <m.div
      initial={{ opacity: 0 }}
      animate={{ opacity: 1 }}
      exit={{ opacity: 0 }}
      className={darkMode ? "dark" : ""}
    >
      <Head>
        <title>EL MHERZI YASSINE Portfolio</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="" />
      </Head>
      <main className="px-10 bg-white dark:bg-gray-900 md:px-20 lg:px-40 ">
        <section className="min-h-screen">
          <UserContext.Provider value={data}>
            <Navbar darkModeState={darkModeState} darkMode={darkMode} />
            <Resume />
            <SocialMedia />
            <Services />
            <Projects />
          </UserContext.Provider>
        </section>
      </main>
    </m.div>
  );
}
